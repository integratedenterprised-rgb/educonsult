/**
 * Visa-risk rule DSL.
 *
 * A rule's `condition` column is a JSON predicate authored by admins. The
 * shape below is intentionally small — five operators and three combinators —
 * so it can be edited from a form without a parser, serialized as-is into
 * Prisma, and round-tripped to an LLM for natural-language → rule synthesis
 * later (see "AI-ready" below).
 *
 * Evaluation is pure and synchronous. The applicant profile is a flat record
 * built from the public form payload; the evaluator dereferences `field` by
 * string lookup. Unknown fields evaluate as `null` and most operators treat
 * `null` as "does not match" — except `notExists`, which matches.
 *
 * AI-ready:
 *   - Field names are stable string keys, listed in PROFILE_FIELDS below.
 *     New rules can be generated by an LLM constrained to those keys.
 *   - Operators are enumerated; no escape hatch, no eval'd expressions.
 *   - The same predicate format is the only thing the engine consumes —
 *     swap the rule source (DB, LLM, A/B variant) without changing the eval.
 */

export type LeafOp =
  | "eq"
  | "ne"
  | "lt"
  | "lte"
  | "gt"
  | "gte"
  | "in"
  | "between"
  | "exists"
  | "notExists";

export interface LeafPredicate {
  kind: "leaf";
  field: string;
  op: LeafOp;
  /** Operator-dependent: scalar for eq/ne/lt/…, [lo, hi] for between, array for in. */
  value?: unknown;
}

export interface AllPredicate {
  kind: "all";
  predicates: Predicate[];
}

export interface AnyPredicate {
  kind: "any";
  predicates: Predicate[];
}

export interface NotPredicate {
  kind: "not";
  predicate: Predicate;
}

export type Predicate = LeafPredicate | AllPredicate | AnyPredicate | NotPredicate;

/**
 * Canonical applicant profile passed to the evaluator. The public form maps
 * its raw payload onto this shape so admin-authored rules reference stable
 * field names regardless of the form's wire format.
 *
 * Keep this list in sync with PROFILE_FIELDS below — the admin editor uses
 * that list to populate the field dropdown so admins can only pick valid
 * keys.
 */
export interface ApplicantProfile {
  // Destination + nationality
  countryCode?: string | null;
  nationalityCode?: string | null;

  // Academic
  gpa?: number | null;
  gpaScale?: number | null; // 4 | 10 | 100 — used to normalize gpa
  educationLevel?: string | null; // "12th" | "Bachelors" | "Masters" | …
  fieldOfStudy?: string | null;
  workExperienceYears?: number | null;

  // English
  ielts?: number | null;
  toefl?: number | null;
  pte?: number | null;
  duolingo?: number | null;

  // Study gap (years between last qualification and intended intake)
  studyGapYears?: number | null;

  // Financials
  showFundsUsd?: number | null;
  sponsorRelation?: string | null; // "self" | "parent" | "sibling" | "agency" | "other"
  fundingSource?: string | null; // "savings" | "loan" | "scholarship" | "mixed"
  hasIncomeTaxReturn?: boolean | null;

  // Visa history
  previousVisaRefusals?: number | null;
  previousVisaCountry?: string | null;

  // Other
  age?: number | null;
  preferredIntake?: string | null;

  /** Anything else the form captures — accessible via `field: "extras.x"`. */
  extras?: Record<string, unknown>;
}

