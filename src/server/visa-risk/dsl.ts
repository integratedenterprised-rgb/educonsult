export type ProfileFieldKey = (typeof PROFILE_FIELDS)[number]["key"];

const FIELD_TYPE = Object.fromEntries(
  PROFILE_FIELDS.map((f) => [f.key, f.type]),
) as Record<string, "string" | "number" | "boolean">;

// ── Evaluator ──────────────────────────────────────────────────────────────

export function evaluate(predicate: Predicate, profile: ApplicantProfile): boolean {
  switch (predicate.kind) {
    case "leaf":
      return evaluateLeaf(predicate, profile);
    case "all":
      return predicate.predicates.every((p) => evaluate(p, profile));
    case "any":
      return predicate.predicates.some((p) => evaluate(p, profile));
    case "not":
      return !evaluate(predicate.predicate, profile);
  }
}

function evaluateLeaf(leaf: LeafPredicate, profile: ApplicantProfile): boolean {
  const raw = resolveField(leaf.field, profile);
  const fieldType = FIELD_TYPE[leaf.field] ?? inferType(raw);

  switch (leaf.op) {
    case "exists":
      return raw !== null && raw !== undefined && raw !== "";
    case "notExists":
      return raw === null || raw === undefined || raw === "";
    case "eq":
      return compareEq(raw, leaf.value, fieldType);
    case "ne":
      // Missing field shouldn't satisfy `ne` — otherwise a rule like
      // "ielts ne 6.5" would fire on every applicant who never took IELTS.
      if (raw === null || raw === undefined || raw === "") return false;
      return !compareEq(raw, leaf.value, fieldType);
    case "lt":
    case "lte":
    case "gt":
    case "gte":
      return compareNumeric(raw, leaf.value, leaf.op);
    case "between": {
      const [lo, hi] = Array.isArray(leaf.value) ? leaf.value : [];
      return compareNumeric(raw, lo, "gte") && compareNumeric(raw, hi, "lte");
    }
    case "in": {
      if (raw === null || raw === undefined) return false;
      const arr = Array.isArray(leaf.value) ? leaf.value : [];
      if (fieldType === "string") {
        const s = String(raw).toLowerCase();
        return arr.some((v) => String(v).toLowerCase() === s);
      }
      return arr.some((v) => compareEq(raw, v, fieldType));
    }
  }
}

/**
 * Look up a (possibly nested) field on the profile. Supports synthetic fields
 * `gpa4` and `englishBand` which are computed from the raw inputs.
 */
function resolveField(field: string, profile: ApplicantProfile): unknown {
  if (field === "gpa4") return normalizeGpa4(profile);
  if (field === "englishBand") return normalizeEnglishBand(profile);
  if (field.startsWith("extras.")) {
    return (profile.extras ?? {})[field.slice("extras.".length)] ?? null;
  }
  return (profile as unknown as Record<string, unknown>)[field] ?? null;
}

function compareEq(a: unknown, b: unknown, fieldType: "string" | "number" | "boolean"): boolean {
  if (a === null || a === undefined) return false;
  if (fieldType === "string") return String(a).toLowerCase() === String(b ?? "").toLowerCase();
  if (fieldType === "number") return Number(a) === Number(b);
  if (fieldType === "boolean") return Boolean(a) === Boolean(b);
  return a === b;
}

function compareNumeric(a: unknown, b: unknown, op: "lt" | "lte" | "gt" | "gte"): boolean {
  if (a === null || a === undefined || b === null || b === undefined) return false;
  const x = Number(a);
  const y = Number(b);
  if (!Number.isFinite(x) || !Number.isFinite(y)) return false;
  if (op === "lt") return x < y;
  if (op === "lte") return x <= y;
  if (op === "gt") return x > y;
  return x >= y;
}

function inferType(v: unknown): "string" | "number" | "boolean" {
  if (typeof v === "number") return "number";
  if (typeof v === "boolean") return "boolean";
  return "string";
}

/**
 * Normalize GPA to a 4.0 scale using `gpaScale` when supplied; otherwise
 * detect heuristically. Returns null if no gpa was provided.
 */
export function normalizeGpa4(profile: ApplicantProfile): number | null {
  const g = profile.gpa;
  if (typeof g !== "number" || !Number.isFinite(g)) return null;
  const scale = profile.gpaScale ?? (g <= 4 ? 4 : g <= 10 ? 10 : 100);
  if (scale === 4) return Math.max(0, Math.min(4, g));
  if (scale === 10) return Math.max(0, Math.min(4, (g / 10) * 4));
  return Math.max(0, Math.min(4, (g / 100) * 4));
}

/** Normalize whichever English test is present to an IELTS-equivalent band (0–9). */
export function normalizeEnglishBand(profile: ApplicantProfile): number | null {
  if (typeof profile.ielts === "number") return profile.ielts;
  if (typeof profile.toefl === "number") return toeflToIelts(profile.toefl);
  if (typeof profile.pte === "number") return pteToIelts(profile.pte);
  if (typeof profile.duolingo === "number") return duolingoToIelts(profile.duolingo);
  return null;
}

function toeflToIelts(t: number): number {
  // Coarse mapping based on ETS/IELTS concordance.
  if (t >= 118) return 9;
  if (t >= 110) return 8.5;
  if (t >= 102) return 8;
  if (t >= 94) return 7.5;
  if (t >= 79) return 7;
  if (t >= 60) return 6.5;
  if (t >= 46) return 6;
  if (t >= 35) return 5.5;
  return 5;
}

function pteToIelts(p: number): number {
  if (p >= 89) return 9;
  if (p >= 84) return 8.5;
  if (p >= 79) return 8;
  if (p >= 73) return 7.5;
  if (p >= 65) return 7;
  if (p >= 58) return 6.5;
  if (p >= 50) return 6;
  if (p >= 42) return 5.5;
  return 5;
}

function duolingoToIelts(d: number): number {
  if (d >= 155) return 9;
  if (d >= 140) return 8;
  if (d >= 125) return 7.5;
  if (d >= 115) return 7;
  if (d >= 105) return 6.5;
  if (d >= 95) return 6;
  if (d >= 85) return 5.5;
  return 5;
}
